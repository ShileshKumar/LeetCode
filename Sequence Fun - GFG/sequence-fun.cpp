class Solution {
	public:
		int NthTerm(int n){
		    // Code  here
            long long int ans=1;
            long long int mod=1e9+7;
            for(long long int i=0;i<=n;i++) {
                ans=(ans*i)+1LL;
                ans=ans%mod;
            }
            return int(ans);
		}

};

